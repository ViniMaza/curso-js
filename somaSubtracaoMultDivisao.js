function calculadoraBasica(numero1, numero2){
    soma = numero1 + numero2
    subtracao = numero1 - numero2
    multiplica = numero1 * numero2
    divisao = numero1 / numero2
    console.log(`O resultado da soma é ${soma}\n
    O resultado da subtração é ${subtracao}\n
    O resultado da multiplicação é ${multiplica}\n
    O resultado da divisão é ${divisao}\n`)
}
calculadoraBasica(10,2)
